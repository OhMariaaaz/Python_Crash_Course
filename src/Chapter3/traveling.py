travel_list = ['Tokyo','Mexico','German','Auriga','Russia']
print("Normal list: " + str(travel_list))
print("Sorted list: " + str(sorted(travel_list)))
print("Normal list: " + str(travel_list))
travel_list.reverse()
print("Reversed list: " + str(travel_list))
travel_list.reverse()
print("Normal list: " + str(travel_list))
travel_list.sort()
print("Sorted list(again): " + str(travel_list))
travel_list.sort(reverse=True)
print("Sorted list(again and reversed): " + str(travel_list))